
-- Símbolo de adição -> + <-

print("A seguir vamos somar 1 + 1 = ")
print(1 + 1)

-- Símbolo de subtração -> - <-

print("2 - 1 = ")
print(2 -1)

-- Símbolo de multiplicação -> * <-

print("3 * 3 =")
print(3 * 3)

-- Símbolo de divisão -> / <-

print("4 / 2 = ")
print(4 / 2)

-- Símbolo de resto de divisão (módulo) -> % <-

print("9 % 3 = ")
print(9 % 3)

print("9 % 2 = ")
print(9 % 2)

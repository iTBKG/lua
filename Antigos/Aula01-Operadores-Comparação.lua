-- Operaão com Maior >

print("2 > 3?")
print(2 > 3)

-- Operaão com Menor <

print("2 < 3?")
print(2 < 3)

-- Operaão com Menor igual <=

print("2 <= 3?")
print(2 <= 3)

-- Operaão com Maior igual >=

print("2 >= 3?")
print(2 >= 3)

-- Operacão com Maior >

print("532 > 3?")
print(532 > 3)

-- Operaão com Maior igual >=

print("3 >= 3?")
print(3 >= 3)

